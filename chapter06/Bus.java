package chapter06;

import java.awt.PageAttributes;

public class Bus {
	
	//멤버변수
	int busNumber;
	int passengerCount;
	int money;
	
	public Bus() {}
	
	public Bus(int busNumber) {
		this.busNumber=busNumber;
	}
	
	public void take(int money) { //매개변수가 승객이 낸돈
		this.money+=money; //버스의 수입 증가
		passengerCount++;
	}
	
	public void showInfo() { //버스의 정보
		System.out.println("버스 "+busNumber+"번의 승객은 "+passengerCount+"명이고, 수입은 "+money+"원 입니다.");
		
	}

}
